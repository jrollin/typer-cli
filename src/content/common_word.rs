/// Common word training support for typing practice
/// 500 most common words per language with frequency weighting

/// A single common word with frequency data
#[derive(Debug, Clone)]
pub struct Word {
    pub text: String,
    /// Frequency weighting for spaced repetition
    #[allow(dead_code)]
    pub frequency: f32, // 0.70-1.00, higher = more common
    /// Word length for future analytics
    #[allow(dead_code)]
    pub length: usize,
}

impl Word {
    pub fn new(text: &str, frequency: f32) -> Self {
        Self {
            text: text.to_string(),
            frequency,
            length: text.chars().count(),
        }
    }
}

