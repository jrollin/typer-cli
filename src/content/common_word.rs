#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_word_count() {
        let english = english_words();
        let french = french_words();

        assert_eq!(english.len(), 500, "English should have 500 words");
        assert_eq!(french.len(), 500, "French should have 500 words");
    }

    #[test]
    fn test_frequency_order_english() {
        let words = english_words();

        // Verify descending frequency order
        for i in 0..words.len() - 1 {
            assert!(
                words[i].frequency >= words[i + 1].frequency,
                "English words should be ordered by frequency: {} ({}) >= {} ({})",
                words[i].text,
                words[i].frequency,
                words[i + 1].text,
                words[i + 1].frequency
            );
        }
    }

    #[test]
    fn test_frequency_order_french() {
        let words = french_words();

        for i in 0..words.len() - 1 {
            assert!(
                words[i].frequency >= words[i + 1].frequency,
                "French words should be ordered by frequency: {} ({}) >= {} ({})",
                words[i].text,
                words[i].frequency,
                words[i + 1].text,
                words[i + 1].frequency
            );
        }
    }

    #[test]
    fn test_word_structure() {
        let english = english_words();
        let french = french_words();

        // Check first English word
        assert_eq!(english[0].text, "the");
        assert_eq!(english[0].frequency, 1.00);
        assert_eq!(english[0].length, 3);

        // Check first French word
        assert_eq!(french[0].text, "le");
        assert_eq!(french[0].frequency, 1.00);
        assert_eq!(french[0].length, 2);
    }

    #[test]
    fn test_frequency_range() {
        let all_words = vec![english_words(), french_words()];

        for words in all_words {
            assert_eq!(
                words[0].frequency, 1.00,
                "First word should have frequency 1.00"
            );
            assert!(
                words.last().unwrap().frequency >= 0.70,
                "Last word should have frequency >= 0.70"
            );
        }
    }

    #[test]
    fn test_word_length_stored() {
        let word = Word::new("test", 0.85);
        assert_eq!(word.length, 4);
        assert_eq!(word.text, "test");
        assert_eq!(word.frequency, 0.85);
    }
}
